    private void guardar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardar
        if(idActual == 0) {
            controlador.insertar(textFieldNombre.getText(), fieldTelefono.getText(), Short.valueOf(comboSemestre.getSelectedItem().toString()), txtDireccion.getText());
        } else {
           controlador.actualizar(idActual, textFieldNombre.getText(), fieldTelefono.getText(), Short.valueOf(comboSemestre.getSelectedItem().toString()), txtDireccion.getText());
        }

        reiniciarFormulario();
        actualizarTabla();
    }//GEN-LAST:event_guardar

