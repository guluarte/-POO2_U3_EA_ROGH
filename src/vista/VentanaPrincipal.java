    private void borrar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrar

        int idSeleccionado = getIdSeleccionado();
        if (idSeleccionado == -1) {
            mostrarMensajeError("Necesitas seleccionar un renglon", "ID Invalido");
            return;
        }
        //confirmar
        controlador.borrar(idSeleccionado);
        actualizarTabla();
        reiniciarFormulario();
    }//GEN-LAST:event_borrar

    private void editar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editar

        int rowSeleccionado = tablaAlumnos.getSelectedRow();
        if (rowSeleccionado == -1) {
             mostrarMensajeError("Necesitas seleccionar un renglon", "ID Invalido");
            return;
        }
        
        DefaultTableModel modelo = (DefaultTableModel) tablaAlumnos.getModel();
        String id = (String) modelo.getValueAt(rowSeleccionado, 0);
        int idSeleccionado = Integer.parseInt(id);
        this.idActual = idSeleccionado;
        this.fieldTelefono.setText((String) modelo.getValueAt(rowSeleccionado, 2));
        int semestre = Integer.parseInt((String) modelo.getValueAt(rowSeleccionado, 3));
        this.comboSemestre.setSelectedIndex(semestre);
        this.textFieldNombre.setText((String) modelo.getValueAt(rowSeleccionado, 1));
        this.txtDireccion.setText((String) modelo.getValueAt(rowSeleccionado, 4));
        
    }//GEN-LAST:event_editar

